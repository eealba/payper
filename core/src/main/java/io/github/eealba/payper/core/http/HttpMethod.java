package io.github.eealba.payper.core.http;

/**
 * Define the Http method supported by the Http Client API
 */
public enum HttpMethod {
    /**
     * Get http method.
     */
    GET,
    /**
     * Post http method.
     */
    POST,
    /**
     * Patch http method.
     */
    PATCH,
    /**
     * Put http method.
     */
    PUT,
    /**
     * Delete http method.
     */
    DELETE;

}
